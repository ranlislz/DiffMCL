class GCN(nn.Module):
    def __init__(self, nfeat, nhid, nout, dropout):
        super(GCN, self).__init__()

        self.gc1 = GraphConvolution(nfeat, nhid)
        self.gc2 = GraphConvolution(nhid, nout)
        self.dropout = dropout

    def forward(self, x, adj):
        x = F.relu(self.gc1(x, adj))  # change to leaky relu
        x = F.dropout(x, self.dropout, training=self.training)
        x = self.gc2(x, adj)
        # x = F.relu(x)
        return x

class GINLayer(nn.Module):
    def __init__(self, in_channels, out_channels):
        super(GINLayer, self).__init__()
        self.linear = nn.Linear(in_channels, out_channels)

    def forward(self, x, adj_matrix):
        x_agg = torch.matmul(adj_matrix, x)
        x = self.linear(x_agg)
        return x

class MultiModalFusionGIN(nn.Module):
    def __init__(self, num_node_features, hidden_channels):
        super().__init__()
        self.gin1 = GINLayer(num_node_features, hidden_channels)
        self.gin2 = GINLayer(hidden_channels, hidden_channels)

    def forward(self, x, adj_matrix):
        x = self.gin1(x, adj_matrix)
        x = F.relu(x)
        x = F.dropout(x, p=0.5, training=self.training)
        x = self.gin2(x, adj_matrix)

        joint_emb = x.sum(dim=0, keepdim=True)

        return x, joint_emb

