class DenoisingDiffusion(nn.Module):
    def __init__(self, emb_size, hidden_size, num_layers, device):
        super(DenoisingDiffusion, self).__init__()
        self.device = device
        self.emb_size = emb_size
        self.model = nn.Sequential(
            nn.Linear(emb_size, hidden_size),
            nn.ReLU(),
            *[nn.Sequential(
                nn.Linear(hidden_size, hidden_size),
                nn.ReLU(),
            ) for _ in range(num_layers - 2)],
            nn.Linear(hidden_size, emb_size)
        )

    def forward(self, emb, noise_scale, num_steps):
        input_size = emb.shape[-1]
        for _ in range(num_steps):
            noise = torch.randn_like(emb) * noise_scale
            noisy_emb = emb + noise
            transform_to_fixed = nn.Linear(input_size, self.emb_size).to(self.device)
            noisy_emb = transform_to_fixed(noisy_emb)
            denoised_emb = self.model(noisy_emb)
            transform_to_original = nn.Linear(self.emb_size, input_size).to(self.device)
            denoised_emb = transform_to_original(denoised_emb)

            # Update noise scale for next iteration (if necessary)
            noise_scale = self.update_noise_scale(noise_scale)

            # Update the emb for the next iteration
            emb = denoised_emb

        return denoised_emb

    def update_noise_scale(self, noise_scale):
        # Update the noise_scale based on your noise schedule strategy
        # For example, linearly decrease the noise_scale
        noise_scale *= 0.9
        return noise_scale

